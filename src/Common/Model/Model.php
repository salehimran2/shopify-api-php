namespace Slince\Shopify\Common\Model;

abstract class Model implements ModelInterface
{
    /**
     * The id of the model.
     *
     * @var string
     */
    protected $id;

    /**
     * {@inheritdoc}
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * {@inheritdoc}
     */
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }
}